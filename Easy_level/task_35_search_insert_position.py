from typing import List

"""
nums = [1, 3, 5, 6], target = 2
nums = [1, 3, 5, 6], target = 7
nums = [1, 3, 5, 6], target = 0

–ò—Ç–µ—Ä–∞—Ü–∏—è 1:
left = 0, right = 3
mid = (0 + 3) // 2 = 1
nums[1] = 3 > 0 ‚Üí –¥–≤–∏–≥–∞–µ–º—Å—è left
right = mid - 1 = 0

–¢–µ–ø–µ—Ä—å: left = 0, right = 0
left <= right ‚Üí —Ü–∏–∫–ª –ø—Ä–æ–¥–æ–ª–∂–∞–µ—Ç—Å—è

–ò—Ç–µ—Ä–∞—Ü–∏—è 2:
left = 0, right = 0
mid = (0 + 0) // 2 = 0
nums[0] = 1 > 0 ‚Üí –¥–≤–∏–≥–∞–µ–º—Å—è left
right = mid - 1 = -1

–¢–µ–ø–µ—Ä—å: left = 0, right = -1
left <= right ‚Üí —Ü–∏–∫–ª –æ—Å—Ç–∞–Ω–∞–≤–ª–∏–≤–∞–µ—Ç—Å—è —Ç–∞–∫ –∫–∞–∫ —É—Å–ª–æ–≤–∏–µ –Ω–µ –≤–µ—Ä–Ω–æ
–∏–Ω–¥–µ–∫—Å –≤—Å—Ç–∞–≤–∫–∏ 0


left = mid + 1 = 2
right = mid - 1 = 1

–¥–≤–∏–≥–∞–µ–º—Å—è –≤–ø—Ä–∞–≤–æ
"""


class Solution:
	def searchInsert(self, nums: List[int], target: int) -> int:
		# –∏–Ω–∏—Ü–∏–∞–ª–∏–∑–∏—Ä–æ–≤–∞—Ç—å left –∏ right
		left = 0
		right  = len(nums) - 1

		while left <= right:
			mid = (left + right) // 2  # –≤—ã—á–∏—Å–ª–∏—Ç—å mid
			if nums[mid] == target:	 # –µ—Å–ª–∏ nums[mid] == target
				return mid
			elif nums[mid] < target: # –µ—Å–ª–∏ nums[mid] < target
				left = mid + 1
			else:					 # –µ—Å–ª–∏ nums[mid] > target
				right = mid - 1
		return left


